class Solution:
    def plusOne(self, digits: list[int]) -> list[int]:
        # ডান দিক থেকে বাম দিকে লুপ চালাচ্ছি
        for i in range(len(digits) - 1, -1, -1):
            if digits[i] < 9:
                digits[i] += 1
                return digits  # কন্ডিশন সত্য হলে এখানেই রেজাল্ট রিটার্ন
            else:
                digits[i] = 0  # ডিজিট ৯ হলে ০ বানিয়ে লুপ পরের ধাপে যাবে

        # যদি [9, 9, 9] এর মতো ইনপুট হয়, তাহলে লুপ শেষে সব [0, 0, 0] হয়ে যাবে।
        # তখন একদম শুরুতে একটা ১ জোড়া দিয়ে ১০০০ বানাতে হবে।
        return [1] + digits

