class Solution:
    def plusOne(self, digits: list[int]) -> list[int]:
        for i in range(len(digits) - 1, -1, -1):
            if digits[i] < 9:
                digits[i] += 1
                return digits
            else:
                digits[i] = 0

        return [1] + digits


if __name__ == "__main__":
    obj = Solution()

    print(obj.plusOne([1, 2, 3]))
    print(obj.plusOne([4, 3, 2, 9]))
    print(obj.plusOne([9, 9, 9]))

