class Solution:
    def isValid(self, s: str) -> bool:
        box = []

        for braket in s:
            if braket == '(' or braket == '{' or braket == '[':
                box.append(braket)

            else:
                if not box:
                    return False

                top = box.pop()

                if braket == ')' and top != '(' :
                    return False
                if braket == '}' and top != '{' :
                    return False
                if braket == ']' and top != '[' :
                    return False

        if not box:
            return True
        else:
            return False

if __name__ == "__main__":
    obj = Solution()

    print(obj.isValid("()[]{}"))
    print(obj.isValid("([])"))
    print(obj.isValid("(]"))


