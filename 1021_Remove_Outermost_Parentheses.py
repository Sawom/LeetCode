class Solution:
    def removeOuterParentheses(self, s: str) -> str:
        box = []
        result = []

        for bracket in s:
            if bracket == '(':
                if len(box) > 0:
                    result.append(bracket)
                box.append(bracket)
            else:
                box.pop()
                if len(box) > 0:
                    result.append(bracket)
        return "".join(result)

